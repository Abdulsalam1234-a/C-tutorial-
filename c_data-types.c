#include <stdio.h>

int main(void)
{
    // Create variables
    int myNum = 5;             // Integer (whole number)
    float myFloatNum = 5.99;   // Floating point number
    char myLetter = 'D';       // Character

    // Print variables
    printf("%c\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);
}